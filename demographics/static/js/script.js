function showtrivia() {
	var btnText = document.getElementById("trivia");
	var other = document.getElementById("demographics");
	  if (btnText.style.display == "none") {
	    btnText.style.display = "block";
	    other.style.display = "none";
	  } else {
	    btnText.style.display = "none";
	  }
}

